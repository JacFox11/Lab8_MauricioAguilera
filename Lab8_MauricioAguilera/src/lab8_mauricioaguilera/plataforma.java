    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        try {
            clientes = new ArrayList();
            productos = new ArrayList();
            clientes.add(new Cliente(0, "Die123", "321ied", "", "", 0));
            db.query.execute("select Cuenta,Name,Password,Products,Identidad,Saldo from clientes");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                clientes.add(new Cliente(rs.getInt(1), rs.getNString(2), rs.getNString(3), rs.getNString(4), rs.getNString(5), rs.getInt(6)));
            }
        } catch (SQLException e) {
        }
        try {
            db.query.execute("select ID,Nombre,Cantidad,Precio,Vencimiento from productos");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                productos.add(new Producto(rs.getInt(1), rs.getNString(2), rs.getInt(3), rs.getInt(4), rs.getNString(5)));
            }
        } catch (SQLException e) {
        }
        for (Cliente t : clientes) {
            if (t.getNombre().equals(tf_user.getText()) && t.getPass().equals(pf_pass.getText())) {
                if (t.getNombre().equals("Die123")) {
                    jd_admin.setModal(true);
                    jd_admin.pack();
                    jd_admin.setLocationRelativeTo(this);
                    jd_admin.setVisible(true);
                } else {
                    DefaultTableModel modelo = (DefaultTableModel) jt_productos.getModel();
                    for (Producto p : productos) {
                        modelo.addRow(new Object[]{p.getNombre(), p.getCantidad(), p.getPrecio(), p.getVencimiento()});
                    }
                    jt_productos.setModel(modelo);
                    
                    jd_cliente.setModal(true);
                    jd_cliente.pack();
                    jd_cliente.setLocationRelativeTo(this);
                    jd_cliente.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

